const productGrid = document.querySelector('#product-grid');
const brandFilter = document.querySelector('#filter-brand');
const typeFilter = document.querySelector('#filter-type');
const stockFilter = document.querySelector('#filter-stock');

const renderProducts = (items) => {
  if (!productGrid) {
    return;
  }
  productGrid.innerHTML = items
    .map(
      (item) => `
      <article class="product-card" data-brand="${item.brand}" data-category="${item.category}" data-stock="${item.availability}">
        <span class="brand-pill">${item.brand}</span>
        <h4>${item.category} ${item.model}</h4>
        <p class="description">${item.description}</p>
        <div class="meta">Наличие: ${item.availability}</div>
        <img src="${item.image}" alt="${item.category} ${item.model}" loading="lazy" />
        <div class="details">${item.details}</div>
        <a class="btn primary price-request" href="#contacts" data-product="${item.model}">Запросить цену</a>
      </article>
    `
    )
    .join('');
};

const populateBrandFilter = () => {
  if (!brandFilter) {
    return;
  }
  const brands = Array.from(new Set(catalogData.map((item) => item.brand))).sort();
  brands.forEach((brand) => {
    const option = document.createElement('option');
    option.value = brand;
    option.textContent = brand;
    brandFilter.appendChild(option);
  });
};

const applyFilters = () => {
  const brandValue = brandFilter?.value ?? 'all';
  const typeValue = typeFilter?.value ?? 'all';
  const stockValue = stockFilter?.value ?? 'all';

  const filtered = catalogData.filter((item) => {
    const brandMatch = brandValue === 'all' || item.brand === brandValue;
    const typeMatch = typeValue === 'all' || item.category === typeValue;
    const stockMatch = stockValue === 'all' || item.availability === stockValue;
    return brandMatch && typeMatch && stockMatch;
  });

  renderProducts(filtered);
};

populateBrandFilter();
applyFilters();

[brandFilter, typeFilter, stockFilter].forEach((filter) => {
  if (filter) {
    filter.addEventListener('change', applyFilters);
  }
});

